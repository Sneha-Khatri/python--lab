# Program 1: Find sum and average of elements in a tuple

# Creating a tuple
numbers = (10, 20, 30, 40, 50)

# Calculating sum
total = sum(numbers)

# Calculating average
average = total / len(numbers)

# Displaying result
print("Tuple elements:", numbers)
print("Sum:", total)
print("Average:", average)
#Output
"""Tuple elements: (10, 20, 30, 40, 50)
Sum: 150
Average: 30.0"""